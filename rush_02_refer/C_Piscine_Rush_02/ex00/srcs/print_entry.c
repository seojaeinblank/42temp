#include <fcntl.h>
#include <unistd.h>

char	print_entry(char *file_name)
{
	int		fd;
	int		n;
	char	buf[500];

	fd = open(file_name, O_RDONLY);
	if (fd < -1)
		return (-1);
	write(1, "-------------------------------------------\n", 44);
	write(1, "\t\tLife is 42!\n", 14);
	write(1, "-------------------------------------------\n", 44);
	while ((n = read(fd, buf, 500)))
		write(1, buf, n);
	close(fd);
	return (0);
}
